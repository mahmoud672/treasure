    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $appointment = Appointment::find($id);
        if($appointment->file_id){
            $filPath=public_path($appointment->file->path);
            unlink($filPath);
        }
        $appointment->delete();
        Session::flash('delete', 'Appointment has been deleted Successfully');
        return redirect(adminUrl('appointment'));
    }

